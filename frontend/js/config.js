// ═══════════════════════════════════════════════════════════════════
// ГЛАВНЫЙ ПЕРЕКЛЮЧАТЕЛЬ — измени на false для продакшена
// ═══════════════════════════════════════════════════════════════════
const DEV_MODE = false;  // Для production установлено false

// ═══════════════════════════════════════════════════════════════════
// ВИЗУАЛЬНЫЕ ЛОГИ — установи false чтобы отключить панель логов
// ═══════════════════════════════════════════════════════════════════
const DEBUG_LOGS = true;  // true = показывать логи на экране

// ═══════════════════════════════════════════════════════════════════
// Мок-данные для разработки (имитация Telegram пользователя)
// ═══════════════════════════════════════════════════════════════════
const DEV_MOCK_USER = {
    id: 999999999,
    first_name: 'Dev',
    last_name: 'User', 
    username: 'dev_user',
    language_code: 'ru',
    is_premium: false,
};

// ═══════════════════════════════════════════════════════════════════
// Конфигурация (экспорт в глобальную область)
// ═══════════════════════════════════════════════════════════════════
window.APP_CONFIG = Object.freeze({
    // Режим разработки
    DEV_MODE: DEV_MODE,
    
    // Мок-пользователь для dev режима
    DEV_MOCK_USER: DEV_MODE ? DEV_MOCK_USER : null,
    
    // API URL (автоматически определяется)
    API_URL: DEV_MODE 
        ? 'http://localhost:3001/api'
        : '/api',
    
    // Пропускать верификацию Telegram в dev режиме
    SKIP_TELEGRAM_VERIFY: DEV_MODE,
    
    // Логирование в консоль
    DEBUG_LOG: DEV_MODE,
    
    // Визуальные логи на экране (панель в углу)
    DEBUG_LOGS: DEBUG_LOGS,
});

// Логируем режим при загрузке
if (window.APP_CONFIG.DEBUG_LOG) {
    console.log('🔧 APP_CONFIG:', window.APP_CONFIG);
    console.log('⚠️  DEV_MODE включён — для продакшена установи DEV_MODE = false в js/config.js');
}

